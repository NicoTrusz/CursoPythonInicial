# Estructuras repetitivas indeterminadas
# Bucle While (hacer mientras)

# Mientras <condicion> hacer
#       <ejecucion por V>
# FinMientras
print("Inicio")
num_1 = 3
while num_1 > 0 : # Ctrl + c finaliza ejecucion
    num_1=num_1-1 # contador (reasignando)
    print("dentro del bucle, vale: ", num_1)

print("Fin")

# Operadores de "asignacion"
# +=  -=  *=  /=
print("Segundo programa\n")
num_2 = 1
print("num_2 vale: ",num_2)
num_2= num_2 + 1 # sumadores
print("num_2 vale: ",num_2)
num_2 += 1 # sumadores // incrementador
print("num_2 vale: ",num_2)

print("Fin Segundo")
